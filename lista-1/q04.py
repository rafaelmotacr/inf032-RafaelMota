# 4. Calcule a área de um círculo de raio r = 2.
# Lembrete: a área de um círculo de raio r é: A∘ = π r2

raioDoCirculo = 2
valorDePI = 3.14 # Considerando valor de PI como 3.14
areaDoCirculo = valorDePI * raioDoCirculo ** 2
print('A área de um círculo de raio ' + str(raioDoCirculo) + ' é igual a: ' + str(areaDoCirculo) + '.')
                                      