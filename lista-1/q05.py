# 5. Quantos segundos há em 3 horas, 23 minutos e 17 segundos?

horas = 3
minutos = 23
segundos = 17
totalSegundos = horas * 60 * 60 + minutos * 60 + segundos

print('O total de segundos em ' + str(horas) + ', ' + str(minutos) + ' minutos e ' + str (segundos) + ' segundos é: ' + str(totalSegundos))
